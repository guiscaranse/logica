valor = int(input("Insira o preço do produto: "))
pago = int(input("Insira o valor pago do produto: "))
troco = int(pago - valor)
ced50 = int((troco - troco%50)/50)
ced20 = int((troco - (ced50*50))/20)
ced10 = int((troco - (ced50*50 + ced20*20))/10)
ced5 = int((troco - (ced50*50 + ced20*20 + ced10*10))/5)
ced2 = int((troco - (ced50*50 + ced20*20 + ced10*10 + ced5*5))/2)
ced1 = int((troco - (ced50*50 + ced20*20 + ced10*10 + ced5*5 + ced2*2))/1)
print("Troco a ser devolvido: ", troco)
print("Cédulas: \nR$50 -", ced50, "\nR$20 - ", ced20, "\nR$10 - ", ced10, "\nR$5 - ", ced5, "\nR$2 - ", ced2, "\nR$1 - ", ced1)
